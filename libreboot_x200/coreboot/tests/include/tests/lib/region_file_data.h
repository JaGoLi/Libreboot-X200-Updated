/* Size (in bytes) of buffer used for testing */
#define REGION_FILE_BUFFER_SIZE (4 * KiB)

void array_dump(void *array, size_t length)
{
	extern int printf(const char *, ...);
	const uint8_t *data = array;

	printf("{");
	for (size_t i = 0; i < length; ++i) {
		if (i % 8 == 0)
			printf("\n\t");

		printf("0x%02x, ", data[i]);
	}
	printf("\n};\n");
}


